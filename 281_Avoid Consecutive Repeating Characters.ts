const modifyString = (s: string): string => {
    const abc = "abcdefghijklmnopqrstuvwxyz";
    let abcArr = abc.split("");
    let pointer = 0;

    const arr = s.split("");

    for (let i = 0; i < arr.length; i++) {
        const letter = arr[i];
        const previous = arr[i - 1];
        const next = arr[i + 1];

        if (letter === "?") {
            while (next === abcArr[pointer] || previous === abcArr[pointer]) {
                pointer++;
                if (pointer > abcArr.length - 1) {
                    pointer = 0;
                }
            }
            arr[i] = abcArr[pointer];
        }
    }

    return arr.join("");
};

